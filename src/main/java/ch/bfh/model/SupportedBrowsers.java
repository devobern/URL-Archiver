package ch.bfh.model;

/**
 * Enumeration of supported web browsers.
 */
public enum SupportedBrowsers {
    /**
     * Mozilla Firefox browser.
     */
    FIREFOX,

    /**
     * Google Chrome browser.
     */
    CHROME,

    /**
     * Microsoft Edge browser.
     */
    EDGE,

    /**
     * Represents a browser type that is not supported.
     */
    UNSUPPORTED,

    /**
     * Default browser setting, used when no specific browser is specified.
     */
    DEFAULT
}
